package model;

public class Niveau {
	private int id;
	private String nomniveau;
	
	public Niveau() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNomniveau() {
		return nomniveau;
	}

	public void setNomniveau(String nomniveau) {
		this.nomniveau = nomniveau;
	}
	
	
}
